#include "libft/libft.h"

int			ft_strlenws(const char *s, char c)
{
	int		i;
	int		k;

	i = 0;
	k = 0;
	while (s[i])
	{
		while (s[i] && (s[i] == c || s[i] == '-'))
			i++;
		k++;
		while (s[i] && s[i] != c && s[i] != '-')
			i++;
	}
	return (k);
}
